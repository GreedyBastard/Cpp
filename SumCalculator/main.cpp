#include <iostream>

using namespace std;

int main()
{
    //Declaring variables
    int a;
    int b;
    int sum;

    //Store data inside variables
    cout << "This is a very simple example of a sum!" << endl;

    cout << "Enter a number! \n";
    cin >> a;

    cout << "Enter another number! \n";
    cin >> b;

    //Calculate the sum and print the answer
    sum = a + b;
    cout << "The sum of those numbers is: " << sum << " potatoes, and I'm sure of that!" << endl;

    return 0;
}
