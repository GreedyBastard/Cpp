#include <iostream>

using namespace std;

int main()
{
    int a;
    int b;
    int sum;

    cout << "This is a very simple example of a sum!" << endl;

    cout << "Enter a number! \n";
    cin >> a;

    cout << "Enter another number! \n";
    cin >> b;

    sum = a + b;
    cout << "The sum of those numbers is: " << sum << endl;

    return 0;
}
